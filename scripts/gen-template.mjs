// 초기 등록용 엑셀 양식 생성기 — `node scripts/gen-template.mjs`.
//  · 비전공자용 단일 시트 양식: '안내'(읽는 법) + '등록'(한 장으로 끝).
//  · '등록' 시트는 ‘품목명’ 열을 기준으로 왼쪽=폴더(분류) 칸 / 오른쪽=품목 필드. ‘>’ 경로·교차참조·타입행 없음.
//  · 동적 필드 타입은 헤더에 한글로: 「면적(숫자)」「계약일(날짜)」 …(숫자·금액·날짜·퍼센트·예아니오). 괄호 없으면 글자.
//  · 산출: public/hierarchy-template.xlsx (dev /dev/import에서 다운로드 링크).
//  · 참고: xlsx 무료 빌드는 셀 색·드롭다운(데이터 유효성)을 못 쓴다 → 친절화는 안내 시트 텍스트 + 컬럼 너비로.
import * as XLSX from 'xlsx';
import { mkdirSync } from 'node:fs';

const wb = XLSX.utils.book_new();

// ── 안내 시트 ── 처음 연 사람이 읽는 사용법(설명 전용, 채우지 않음).
const guide = [
  ['이 파일 채우는 법 — 먼저 읽어주세요'],
  [''],
  ['1. ‘등록’ 시트 한 장만 채우면 됩니다. (이 안내 시트는 설명용)'],
  ['2. 왼쪽 ‘대분류 / 중분류’ 칸에 폴더(분류)를 적으세요. 왼쪽이 큰 분류, 오른쪽이 작은 분류입니다.'],
  ['     예) 대분류=현장,  중분류=강남 현장'],
  ['3. 같은 폴더에 품목이 여러 개면, 폴더 칸을 똑같이 반복해 적고 ‘품목명’부터 오른쪽에 내용을 채우세요.'],
  ['4. 폴더만 먼저 만들고 싶으면 폴더 칸만 적고 ‘품목명’은 비워 두세요. (예시의 ‘판교 현장’ 줄)'],
  ['5. 더 깊은 분류가 필요하면 ‘중분류’와 ‘품목명’ 사이에 칸을 끼워 넣으세요. 한 단계 더 깊어집니다.'],
  ['6. 아래 예시 줄은 지우고 실제 데이터를 넣으세요.'],
  [''],
  ['입력값 안내'],
  ['· 배지색: 성공 / 경고 / 위험 / 정보 / 기본 중 하나를 적으세요. (비우면 ‘기본’. ‘배지’ 칸이 비면 배지는 안 보입니다.)'],
  ['· 헤더의 (숫자)(금액)(날짜)(퍼센트)(예아니오)는 그 칸에 넣을 값의 종류입니다. 헤더 글자는 바꾸지 마세요.'],
  ['· 칸을 더 만들고 싶으면 맨 오른쪽에 「이름(숫자)」처럼 헤더를 추가하면 그 칸이 품목 정보로 들어갑니다.'],
  ['· ‘품목명’ 열은 반드시 있어야 합니다. 지우지 마세요.'],
];
const gws = XLSX.utils.aoa_to_sheet(guide);
gws['!cols'] = [{ wch: 96 }];
XLSX.utils.book_append_sheet(wb, gws, '안내');

// ── 등록 시트 ── 한 장으로 끝. 품목명 왼쪽=폴더 칸, 오른쪽=품목 필드(고정 + 동적).
const reg = [
  ['대분류', '중분류', '품목명', '부제', '배지', '배지색', '썸네일', '면적(숫자)', '계약일(날짜)', '담당'],
  ['현장', '강남 현장', '거실 도면', 'rev.2', '승인', '성공', '', 32, '2026-05-02', '김병준'],
  ['현장', '강남 현장', '주방 도면', 'rev.1', '검토중', '경고', '', 18, '2026-05-10', '이수연'],
  ['현장', '판교 현장', '', '', '', '', '', '', '', ''],          // ← 폴더만 만드는 줄(빈 폴더)
  ['거래처', '가구상사', '계약서', '', '확정', '성공', '', '', '2026-04-01', '박지훈'],
];
const rws = XLSX.utils.aoa_to_sheet(reg);
rws['!cols'] = [10, 12, 16, 10, 8, 8, 10, 12, 14, 10].map((wch) => ({ wch }));
XLSX.utils.book_append_sheet(wb, rws, '등록');

mkdirSync('public', { recursive: true });
XLSX.writeFile(wb, 'public/hierarchy-template.xlsx');
console.log('✓ public/hierarchy-template.xlsx 생성 (안내 + 등록 시트)');
